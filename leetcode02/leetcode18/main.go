/**
最短的桥
*/
func shortestBridge(grid [][]int) int {
	m := len(grid)
	n := len(grid[0])
	points := make([][]int, 0)

	find := false
	for i := 0; i < m; i++ {
		for j := 0; j < n; j++ {
			if grid[i][j] == 1 && !find {
				dfs(&grid, i, j, &points)
				find = true
				break
			}
		}
	}

	i := bfs(&points, &grid)
	return i
}

func bfs(points *[][]int, grid *[][]int) int {
	queue := make([][]int, 0)
	for _, point := range *points {
		queue = append(queue, point)
	}

	level := 0
	for len(queue) > 0 {
		size := len(queue)
		for i := 0; i < size; i++ {
			e := queue[0]
			queue = queue[1:]

			x := e[0]
			y := e[1]

			if x >= 0 && x < len(*grid) && y >= 0 && y < len((*grid)[0]) {
				if (*grid)[x][y] == 2 || (*grid)[x][y] == 3 {
					continue
				}
				if (*grid)[x][y] == 1 {
					return level
				}

				(*grid)[x][y] = 3
				queue = append(queue, []int{x + 1, y})
				queue = append(queue, []int{x - 1, y})
				queue = append(queue, []int{x, y + 1})
				queue = append(queue, []int{x, y - 1})
			}
		}
		level++
	}
	return level
}

func dfs(grid *[][]int, i int, j int, points *[][]int) {
	if i < 0 || i >= len(*grid) || j < 0 || j >= len((*grid)[0]) || (*grid)[i][j] == 2 {
		return
	}
	if (*grid)[i][j] == 0 {
		*points = append(*points, []int{i, j})
		return
	}
	if (*grid)[i][j] == 1 {
		(*grid)[i][j] = 2
	}

	dfs(grid, i+1, j, points)
	dfs(grid, i-1, j, points)
	dfs(grid, i, j+1, points)
	dfs(grid, i, j-1, points)
}

